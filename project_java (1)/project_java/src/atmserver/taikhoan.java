    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String loi="";
        String password = txtmkmoi.getText();
        String repassword = txtxacnhan.getText();
        // validate..............
        if (password.length()==0 && repassword.length()==0) {
            loi+="\n Mật khẩu Hoặc xác nhận mật khẩu không được để trống!!";
        }else{
            if(!password.equals(repassword)){
            loi+="\n Xác nhận mật khẩu không chính xác";
        }
            else if (!untity.checkPass(txtmkmoi.getText())) {
                loi+="\n Mật khẩu phải là số và chỉ được 6 kí tự!!!";
            }
        }
        if (loi.length()==0) {
            Connection con = null;
            ResultSet rs = null;
            CallableStatement cstmt = null;
            con = DBConnect.opConnection();
            try {
                cstmt = con.prepareCall("{call updatePass (?,?)}");
                cstmt.setString(1, MaKH.getText());
                cstmt.setString(2, txtmkmoi.getText());
                int i = cstmt.executeUpdate();
                if (i>0) {
                    JOptionPane.showMessageDialog(null, "Đổi mật khẩu thành công");
                    ThongTinKH tt = new ThongTinKH();
                    Access.CanleAcess(tt, this);
                }else{
                    JOptionPane.showMessageDialog(null, "Đổi mật khẩu thất bại!!!","THÔNG BÁO",JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(taikhoan.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                DBConnect.closeAll(con, cstmt, rs);
            }
        }else{
            JOptionPane.showMessageDialog(null, loi);
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

