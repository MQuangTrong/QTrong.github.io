    public  void loadMaKH(String Sotaikhoan){
        Connection con = null;
        ResultSet rs = null;
        CallableStatement cstmt = null;
        con = DBConnect.opConnection();
        try {
            cstmt = con.prepareCall("{call getCustomerId (?)}");           
            cstmt.setString(1,Sotaikhoan);
            rs = cstmt.executeQuery();
            while (rs.next()) {                
                MaKH.setText(Sotaikhoan);
                txtmatkhau.setText(rs.getString("Password"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(taikhoan.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            DBConnect.closeAll(con, cstmt, rs);
        }
    }
